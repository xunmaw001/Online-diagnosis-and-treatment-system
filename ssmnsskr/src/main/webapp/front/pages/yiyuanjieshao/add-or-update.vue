				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`yiyuanjieshao`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='yiyuanmingcheng'){
					this.ruleForm.yiyuanmingcheng = obj[o];
					this.ro.yiyuanmingcheng = true;
					continue;
					}
					if(o=='yiyuandizhi'){
					this.ruleForm.yiyuandizhi = obj[o];
					this.ro.yiyuandizhi = true;
					continue;
					}
					if(o=='dengji'){
					this.ruleForm.dengji = obj[o];
					this.ro.dengji = true;
					continue;
					}
					if(o=='yiyuanjieshao'){
					this.ruleForm.yiyuanjieshao = obj[o];
					this.ro.yiyuanjieshao = true;
					continue;
					}
					if(o=='lianxidianhua'){
					this.ruleForm.lianxidianhua = obj[o];
					this.ro.lianxidianhua = true;
					continue;
					}
					if(o=='shebeijieshao'){
					this.ruleForm.shebeijieshao = obj[o];
					this.ro.shebeijieshao = true;
					continue;
					}
					if(o=='zhuceriqi'){
					this.ruleForm.zhuceriqi = obj[o];
					this.ro.zhuceriqi = true;
					continue;
					}
					if(o=='tupian'){
					this.ruleForm.tupian = obj[o];
					this.ro.tupian = true;
					continue;
					}
				}
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},
																																																						
			// 多级联动参数
																																																						
																																													zhuceriqiChange(e) {
				this.ruleForm.zhuceriqi = e.target.value;
				this.$forceUpdate();
			},
												
																																																			
																					// 下拉变化
			dengjiChange(e) {
				this.dengjiIndex = e.target.value
				this.ruleForm.dengji = this.dengjiOptions[this.dengjiIndex]
			},
																																				
																																																																								tupianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.tupian = _this.$base.url + 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},
									
			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
																																																																																																																																																																																																																																																																																																																																																																																																																																																																																												if(this.ruleForm.lianxidianhua&&(!this.$validate.isMobile(this.ruleForm.lianxidianhua))){
					this.$utils.msg(`联系电话应输入手机格式`);
					return
				}
																																																																																																																																				if(this.ruleForm.id){
					await this.$api.update(`yiyuanjieshao`, this.ruleForm);
				}else{
					await this.$api.add(`yiyuanjieshao`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

